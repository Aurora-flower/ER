/**
 * @file 国际化
 * @description
 * Intl 对象是 ECMAScript 国际化 API 的一个命名空间，它提供了精确的字符串对比、数字格式化，和日期时间格式化。
 * `Collator`，`NumberFormat` 和 `DateTimeFormat` 对象的构造函数是 Intl 对象的属性。
 * @remarks
 * - 属性
 *
 *    - `Intl.Collator`
 *    collators 的构造函数，用于启用对语言敏感的字符串比较的对象。
 *
 *    - `Intl.DateTimeFormat`
 *    用于启用语言敏感的日期和时间格式的对象的构造函数。
 *
 *    - `Intl.ListFormat`
 *    用于启用语言敏感(language-sensitive)列表格式的对象的构造函数。
 *
 *    - `Intl.NumberFormat`
 *    用于启用语言敏感数字格式的对象的构造函数。
 *
 *    - `Intl.PluralRules`
 *    用于启用多种敏感格式和多种语言语言规则的对象的构造函数。
 *
 *    - `Intl.RelativeTimeFormat`
 *    用于启用语言敏感的相对时间格式的对象的构造函数。
 *
 * - 方法
 *
 *    - `Intl.getCanonicalLocales()`
 *    返回规范区域名称（canonical locale name）。
 */
